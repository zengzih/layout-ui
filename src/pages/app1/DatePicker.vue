      type="date"
      placeholder="选择日期"
      :picker-options="pickerOptions1">
    </el-date-picker>
	  </div>
    </block>
	
	<block sc-title='其他日期单位'>
	<div class="demo-block-wrap2">
	  <div class="demo-block">
	  <span class="demonstration">周</span>
  <el-date-picker
    v-model="value3"
    type="week"
    format="yyyy 第 WW 周"
    placeholder="选择周">
  </el-date-picker>
	</div>
	
	<div class="demo-block">
		<span class="demonstration">月</span>
  <el-date-picker
    v-model="value4"
    type="month"
    placeholder="选择月">
  </el-date-picker>
	</div>
	
	<div class="demo-block">
		 <span class="demonstration">年</span>
  <el-date-picker
    v-model="value5"
    align="right"
    type="year"
    placeholder="选择年">
  </el-date-picker>
	</div>
	
	</div>
    </block>
	
	<block sc-title='选择日期范围' class="demo-block-wrap1">
	  <div class="demo-block">
